package celestibytes.celestialwizardry.reference;

public class Characters {
	public static final byte CR = 0x0D;         // 13
	public static final byte LF = 0x0A;         // 10
	public static final byte SPECIAL = 0x26;    // 38 '&'
	public static final byte WHITESPACE = 0x20; // 32 ' '
	public static final byte BACKSLASH = 0x5C;  // 92 '\'
	public static final byte COMMENT = 0x23;    // 35 '#'
	
	public static final byte RUNE = 'R';
	public static final byte IMAGE = 'I';
	public static final byte RECIPE = 'C';
	
	public static final byte NEWLINE = 'N';
	public static final byte HORIZ_LINE = 'H';
}
